int		main(int argc, char **argv)
{
	if (argc == 2)
	{
		if (!is_valid(argv[1]))
		{
			print_error();
			return (-1);
		}
		fillit(argv[1]);
	}
	else
	{
		print_usage();
	}
	return (0);
}
