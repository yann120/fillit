#include "libft.h"

int			ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*ss1;
	unsigned char	*ss2;
	size_t			i;

	i = 0;
	ss1 = (unsigned char *)s1;
	ss2 = (unsigned char *)s2;
	while (i < n && *ss1 == *ss2)
	{
		i++;
		ss1++;
		ss2++;
	}
	if (i == n)
		return (0);
	return (*ss1 - *ss2);
}
